    private void loadFromJson(String roleFilter, String idFilter) {
      String filepath = "src\\Main\\employee.json"; // Simplified path
        JSONParser parser = new JSONParser();
        DefaultTableModel model = (DefaultTableModel) AccTable.getModel();
        model.setRowCount(0);

        try (FileReader reader = new FileReader(filepath)) {
            JSONObject data = (JSONObject) parser.parse(reader);
            JSONArray employees = (JSONArray) data.get("employee");

            for (Object obj : employees) {
                JSONObject emp = (JSONObject) obj;
                String id = emp.get("id") != null ? emp.get("id").toString() : "";
                String username = emp.get("username") != null ? emp.get("username").toString() : "";
                String role = emp.get("role") != null ? emp.get("role").toString() : "";

                boolean roleMatches = roleFilter == null || roleFilter.equals("All") || role.equalsIgnoreCase(roleFilter);
                boolean idMatches = idFilter == null || id.equalsIgnoreCase(idFilter);

                if (roleMatches && idMatches) {
                    model.addRow(new Object[]{id, username, role});
                }
            }

        } catch (IOException | ParseException e) {
            JOptionPane.showMessageDialog(this, "Failed to load accounts: " + e.getMessage());
        }
    }
    
    

