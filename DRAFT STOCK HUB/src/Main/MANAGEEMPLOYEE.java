    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = AccTable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select an account to edit.");
            return;
        }

        String selectedId = AccTable.getValueAt(selectedRow, 0).toString();
        String selectedName = AccTable.getValueAt(selectedRow, 1).toString();
        String selectedRole = AccTable.getValueAt(selectedRow, 2).toString();
        String selectedPassword = "";

        String filepath = "src\\Main\\employee.json"; // Simplified path

        JSONParser parser = new JSONParser();
        try (FileReader reader = new FileReader(filepath)) {
            JSONObject data = (JSONObject) parser.parse(reader);
            JSONArray employees = (JSONArray) data.get("employee");

            for (Object obj : employees) {
                JSONObject emp = (JSONObject) obj;
                String id = emp.get("id") != null ? emp.get("id").toString() : "";
                if (id.equals(selectedId)) {
                    selectedPassword = emp.get("password") != null ? emp.get("password").toString() : "";
                    break;
                }
            }

            EDITACC x = new EDITACC();
            x.setEditMode(selectedId, selectedName, selectedPassword, selectedRole);
            x.setVisible(true);
            x.addWindowListener(new java.awt.event.WindowAdapter() {
                public void windowClosed(java.awt.event.WindowEvent e) {
                    refreshTable();
                }
            });

        } catch (IOException | ParseException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading employee data.");
        }
    }//GEN-LAST:event_EditBtnActionPerformed

